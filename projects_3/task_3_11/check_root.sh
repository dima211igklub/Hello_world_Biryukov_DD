#!/bin/bash


check_root() {
    
    if [ "$EUID" -eq 0 ]; then
        echo "✅ Скрипт запущен от имени суперпользователя (root)"
        return 0
    else
        echo "❌ ПРЕДУПРЕЖДЕНИЕ: Скрипт должен быть запущен от имени суперпользователя!"
        echo "   Ваш UID: $EUID"
        echo "   Пожалуйста, запустите скрипт с помощью sudo или от имени root."
        exit 1
    fi
}


echo "========================================="
echo "   ПРОВЕРКА ПРАВ СУПЕРПОЛЬЗОВАТЕЛЯ"
echo "========================================="
echo ""


check_root


echo ""
echo "Продолжение выполнения скрипта..."
echo "Выполняются операции, требующие прав администратора."


echo ""
echo "✅ Все операции успешно завершены."
