#!/bin/bash

FILE="students.txt"

echo "========================================="
echo "         СТАТИСТИКА ОЦЕНОК"
echo "========================================="
echo ""


if [ ! -f "$FILE" ]; then
    echo "❌ Ошибка: Файл $FILE не найден!"
    echo "   Сначала создайте файл students.txt с данными студентов."
    exit 1
fi


echo "📊 1. Сумма всех оценок:"
echo "----------------------------------------"
SUM=$(awk '{sum+=$2} END {print sum}' "$FILE")
echo "   Сумма: $SUM баллов"

echo ""
echo "========================================="
echo ""

echo "📈 2. Средняя оценка:"
echo "----------------------------------------"
AVG=$(awk '{sum+=$2} END {printf "%.2f", sum/NR}' "$FILE")
echo "   Средняя оценка: $AVG баллов"

echo ""
echo "========================================="
echo ""


echo "🏆 3. Максимальная оценка:"
echo "----------------------------------------"
MAX=$(awk 'NR==1{max=$2} $2>max{max=$2} END {print max}' "$FILE")
MAX_NAME=$(awk -v max="$MAX" '$2==max {print $1}' "$FILE")
echo "   Максимальная оценка: $MAX баллов"
echo "   Студент: $MAX_NAME"

echo ""
echo "========================================="
