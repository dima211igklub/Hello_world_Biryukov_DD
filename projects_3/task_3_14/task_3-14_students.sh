#!/bin/bash

FILE="students.txt"

echo "========================================="
echo "         АНАЛИЗ ФАЙЛА students.txt"
echo "========================================="
echo ""


echo "📋 1. Имена студентов:"
echo "----------------------------------------"
cut -d' ' -f1 "$FILE"


echo ""
echo "========================================="
echo ""


echo "📊 2. Оценки студентов:"
echo "----------------------------------------"
cut -d' ' -f2 "$FILE"


echo ""
echo "========================================="
echo ""


echo "🔢 3. Номер строки и имя студента:"
echo "----------------------------------------"
awk '{print NR ". " $1}' "$FILE"


echo ""
echo "========================================="

