#!/bin/bash

FILE="data.csv"

echo "========================================="
echo "         АНАЛИЗ CSV-ФАЙЛА"
echo "========================================="
echo ""


if [ ! -f "$FILE" ]; then
    echo "❌ Ошибка: Файл $FILE не найден!"
    echo "   Сначала создайте файл data.csv с данными товаров."
    exit 1
fi


echo "📦 1. Названия товаров:"
echo "----------------------------------------"
awk -F',' '{print "   " $2}' "$FILE"

echo ""
echo "========================================="
echo ""


echo "💰 2. Товары дороже 20:"
echo "----------------------------------------"
awk -F',' '$3 > 20 {printf "   %-10s - %3d руб.\n", $2, $3}' "$FILE"

echo ""
echo "========================================="
echo ""


echo "💵 3. Общая стоимость всех товаров:"
echo "----------------------------------------"
SUM=$(awk -F',' '{sum+=$3} END {print sum}' "$FILE")
echo "   Общая стоимость: $SUM руб."

echo ""
echo "========================================="
