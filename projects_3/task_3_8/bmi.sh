#!/bin/bash


echo "================================="
echo "   Калькулятор индекса массы тела (ИМТ)"
echo "================================="
echo ""

read -p "Введите вашу массу (в кг, например: 75): " WEIGHT


read -p "Введите ваш рост (в метрах, например: 1.75): " HEIGHT


if [[ ! "$WEIGHT" =~ ^[0-9]+([.][0-9]+)?$ ]] || [[ ! "$HEIGHT" =~ ^[0-9]+([.][0-9]+)?$ ]]; then
    echo ""
    echo "ОШИБКА: Масса и рост должны быть числами!"
    echo "Пример: масса = 75, рост = 1.75"
    exit 1
fi


if (( $(echo "$HEIGHT <= 0" | bc -l) )); then
    echo ""
    echo "ОШИБКА: Рост должен быть больше нуля!"
    exit 1
fi

BMI=$(echo "scale=0; $WEIGHT / ($HEIGHT * $HEIGHT)" | bc)


echo ""
echo "================================="
echo "Результат расчета:"
echo "Масса: $WEIGHT кг"
echo "Рост: $HEIGHT м"
echo "Ваш ИМТ: $BMI"
echo "================================="


echo ""
echo "Интерпретация ИМТ:"
if [ $BMI -lt 16 ]; then
    echo "  Выраженный дефицит массы тела"
elif [ $BMI -lt 18.5 ]; then
    echo "  Дефицит массы тела"
elif [ $BMI -lt 25 ]; then
    echo "  Нормальная масса тела"
elif [ $BMI -lt 30 ]; then
    echo "  Избыточная масса тела (предожирение)"
elif [ $BMI -lt 35 ]; then
    echo "  Ожирение I степени"
elif [ $BMI -lt 40 ]; then
    echo "  Ожирение II степени"
else
    echo "  Ожирение III степени (морбидное)"
fi
